uint32_t last_tick;
#define WINDSPEED_MAX 400
void EXTI4_IRQHandler(void)
{
	uint32_t new_tick = osKernelSysTick(); // Syscall!
	wind_speed_tout_tim = WINDSPEED_TIMEOUT; // kick the timer

	if(__HAL_GPIO_EXTI_GET_FLAG(GPIO_PIN_4)) {
		__HAL_GPIO_EXTI_CLEAR_FLAG(GPIO_PIN_4);
//		HAL_GPIO_TogglePin(LD2_GPIO_Port, LD2_Pin);

		uint32_t delta_t = new_tick - last_tick;

		// avoid division by 0
		if (delta_t == 0) {
			delta_t = 1;
		}

		last_tick = new_tick;
		wind_speed = 4000 / delta_t;

		if (wind_speed > WINDSPEED_MAX)
			wind_speed = WINDSPEED_MAX;
	}
}

unsigned get_windspeed(void)
{
	return wind_speed;
}
